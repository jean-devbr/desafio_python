#Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo
base = int(input('Digite um número inteiro para a base do retângulo: '))
altura = int(input('Digite um número inteiro para altura do retângulo: '))
area = base * altura 
print(f'As dimensôes do retângulo é altura {altura} e base {base}.Área do retângulo é {area}')